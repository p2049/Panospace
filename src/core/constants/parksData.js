// Helper function to get park by ID
export const getParkById = (parkId) => {
    return PARKS_DATA.find(park => park.parkId === parkId);
};

// Helper function to get parks by type
export const getParksByType = (parkType) => {
    return PARKS_DATA.filter(park => park.parkType === parkType);
};

// Helper function to get all park IDs
export const getAllParkIds = () => {
    return PARKS_DATA.map(park => park.parkId);
};

// Helper function to get parks by state
export const getParksByState = (state) => {
    return PARKS_DATA.filter(park => park.state.includes(state));
};
