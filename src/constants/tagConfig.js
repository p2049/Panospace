/**
 * Get tags by category
 */
export const getTagsByCategory = (category) => {
    switch (category) {
        case 'aesthetic':
            return AESTHETIC_TAGS;
        case 'style':
            return STYLE_TAGS;
        case 'subject':
            return SUBJECT_TAGS;
        case 'technical':
            return TECHNICAL_TAGS;
        case 'gear':
            return GEAR_TAGS;
        case 'event':
            return EVENT_TAGS;
        case 'film':
            return FILM_TAGS;
        case 'location':
            return LOCATION_TAGS;
        case 'nature_type':
            return NATURE_TYPE_TAGS;
        case 'mood':
            return MOOD_TAGS;
        case 'season':
            return SEASON_TAGS;
        case 'time':
            return TIME_OF_DAY_TAGS;
        case 'color':
            return COLOR_TAGS;
        default:
            return [];
    }
};

